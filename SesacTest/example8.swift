//조건문 (if, switch)
var gender = true

if gender == true {
  print("저는 남자입니다.")
} else {
  print("저는 여자입니다.")
}

// 수화물 요금 안내
var weight = 20

if weight > 15 {
  print("추가 요금을 내세요.")
} else {
  print("추가 요금은 없습니다.")
}

// 부모님 동의가 필요한 회원가입
var age = 50

if age >= 14 {
  print("회원가입 성공")
} else {
  print("부모님 동의가 필요합니다.")
}

// 영화 프로그램 추천
var userSelect = "영화"

if userSelect != "영화" {
  print("오늘의 드라마 '어게인 마이 라이프'을 추천드립니다.")
} else {
  print("오늘의 영화 '괴물'을 추천드려요")
}

var gradePoint = 4.4

if gradePoint >= 4.3 {
  print("성적 장학금을 받을 수 있습니다.")
} else {
  print("성적 장학금 대상이 아닙니다.")
}